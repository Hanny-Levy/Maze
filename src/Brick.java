import javax.swing.*;
import java.awt.*;

public class Brick extends JPanel {
    private int x ;
    private int y;
    private int width;
    private int height;



    public Brick(int x, int y, int width, int height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;

    }




    @Override
    public void print(Graphics graphics) {
        super.print(graphics);
        graphics.fillRect(this.x,this.y,this.width,this.height);
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    @Override
    public int getX() {
        return x;
    }

    @Override
    public int getY() {
        return y;
    }

    @Override
    public int getWidth() {
        return width;
    }

    @Override
    public int getHeight() {
        return height;
    }




}
